public class Estudante {
    private String nome;
    private int idade;
    private int pontos;

    public String getNome(){
       return nome;
    }
    public int getIdade(){
        return idade;
    }
    public int getPontos(){
        return pontos;
    }
    public void setNome(String nome){
        this.nome = nome;
    }
    public void setIdade(int idade){
        this.idade = idade;
    }
    public void setPontos(int pontos){
        this.pontos = pontos;
    }
}
